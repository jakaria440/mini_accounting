ভ্রাতৃত্ব ও পারস্পরিক সহযোগিতা প্রতিষ্ঠা:
ইসলামে মুসলমানদের পরস্পর ভাই বলা হয়েছে— "নিশ্চয়ই মুমিনগণ পরস্পর ভাই ভাই" (সূরা হুজরাত: ১০)। আমরা চাই এই তহবিলের মাধ্যমে একে অপরের পাশে দাঁড়িয়ে আমাদের ঈমানি ও সামাজিক দায়িত্ব পালন করতে।
